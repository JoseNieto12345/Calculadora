public class Suma{
    private int numero1;
    private int numero2;

    public Suma(int numero1, int numero2){
        this.numero1=numero2;
        this.numero2=numero1;

    }
    public int result(){
        return numero1+numero2;

    }
    public int getNum1(){
        return numero1;
    }
    public int getNum2(){
        return numero2;
    }

}










